const UploadProducts = () => {
  useEffect(() => {
    uploadCategoryProducts('Casual', casualproducts);
    uploadCategoryProducts('Formal', formalProducts);
    uploadCategoryProducts('Signature', signature);
    uploadCategoryProducts('Cords', cords);
    uploadCategoryProducts('Wedding', wedding);
    uploadCategoryProducts('Saree', saree);
  }, []);

  const uploadCategoryProducts = async (categoryName, productArray) => {
    for (const product of productArray) {
      try {
        // STEP 1: POST to category node to get unique ID
        const categoryRes = await fetch(
          `https://cheriecloset-default-rtdb.firebaseio.com/${categoryName}.json`,
          {
            method: 'POST', // ✅ Use POST, not PUT
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(product),
          }
        );

        if (!categoryRes.ok) {
          console.log(`❌ Failed to upload ${product.name} to /${categoryName}`);
          continue;
        }

        const categoryData = await categoryRes.json();
        const newProductId = categoryData.name; // Firebase generated key

        // STEP 2: Also POST to All Products with same ID
        const allProductObj = {
          ...product,
          category: categoryName,
        };

        await fetch(
          `https://cheriecloset-default-rtdb.firebaseio.com/All Products/${newProductId}.json`,
          {
            method: 'PUT',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(allProductObj),
          }
        );

        console.log(`✅ Uploaded ${product.name} to /${categoryName} and /All Products`);
      } catch (error) {
        console.error(`🔥 Error uploading ${product.name}:`, error.message);
      }
    }
  };

  return null;
};

export default UploadProducts;
